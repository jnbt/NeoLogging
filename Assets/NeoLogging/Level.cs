namespace Neo.Logging{
  /// <summary>
  /// Describes the level of logging for instances of the logger class
  /// </summary>
  public enum Level{
    /// <summary>
    /// Describes a logging on debug level
    /// </summary>
    LOG,
    /// <summary>
    /// Describes a logging on warning level
    /// </summary>
    WARN, 
    /// <summary>
    /// Describes a logging on error level
    /// </summary>
    ERROR
  }
}
